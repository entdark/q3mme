void UI_FontRow( char c, int row, char *data ) {
	int x;
	const unsigned char f = font7x14[c * FONT_HEIGHT+row];
	
	for (x = 0; x < FONT_WIDTH ; x++ ) {
		*data++ = f & ( 1 << x);
	}
}

void UI_FontColumn( char c, int col, char *data ) {
	int y;
	const unsigned char *f = &font7x14[c * FONT_HEIGHT];
	
	col = (0x1 << FONT_WIDTH) >> col;
	for (y = 0; y < FONT_HEIGHT; y++, f++ ) {
		*data++ = f[0] & col;
	}
}


