/*
================
CG_FireWeapon

Caused by an EV_FIRE_WEAPON event
================
*/
void CG_FireWeapon( centity_t *cent ) {
	entityState_t *ent;
	int				c;
	weaponInfo_t	*weap;
	const cgWeaponFX_t	*weaponFX;

	ent = &cent->currentState;
	if ( ent->weapon == WP_NONE ) {
		return;
	}
	if ( ent->weapon >= WP_NUM_WEAPONS ) {
		CG_Error( "CG_FireWeapon: ent->weapon >= WP_NUM_WEAPONS" );
		return;
	}
	weap = &cg_weapons[ ent->weapon ];
	weaponFX = CG_FindWeaponFX( ent->weapon );

	// mark the entity as muzzle flashing, so when it is added it will
	// append the flash to the weapon model
	cent->muzzleFlashTime = cg.time;

	// lightning gun only does this this on initial press
	if ( ent->weapon == WP_LIGHTNING ) {
		if ( cent->pe.lightningFiring ) {
			return;
		}
	} else if ( ent->weapon == WP_RAILGUN ) {
		cent->pe.railFireTime = cg.time;
	}
	// play quad sound if needed
	if ( cent->currentState.powerups & ( 1 << PW_QUAD ) ) {
		trap_S_StartSound (NULL, cent->currentState.number, CHAN_ITEM, cgs.media.quadSound );
	}

	if ( weaponFX->fire ) {
		fxParent_t fxParent;

		VectorCopy( cent->currentState.pos.trDelta, fxParent.velocity );
		AngleVectors( cent->lerpAngles, fxParent.dir, 0, 0 );

		VectorMA( cent->lerpOrigin, 14, fxParent.dir, fxParent.origin );
		fxParent.origin[2] += cent->pe.viewHeight;

		fxParent.flags = FXP_ORIGIN | FXP_DIR | FXP_VELOCITY;

		trap_FX_Run( weaponFX->fire, &fxParent, cent );
	}

	// play a sound
	for ( c = 0 ; c < 4 ; c++ ) {
		if ( !weap->flashSound[c] ) {
			break;
		}
	}
	if ( c > 0 ) {
		c = rand() % c;
		if ( weap->flashSound[c] )
		{
			trap_S_StartSound( NULL, ent->number, CHAN_WEAPON, weap->flashSound[c] );
		}
	}

	// do brass ejection
	if ( weap->ejectBrassFunc && cg_brassTime.integer > 0 ) {
		weap->ejectBrassFunc( cent );
	}
}


/*
=================
CG_MissileHitWall

Caused by an EV_MISSILE_MISS event, or directly by local bullet tracing
=================
*/
void CG_MissileHitWall( int weapon, int clientNum, vec3_t origin, vec3_t dir, impactSound_t soundType ) {
	fxHandle_t fxHandle;
	fxParent_t fxParent;

	VectorCopy( origin, fxParent.origin );
	VectorCopy( dir, fxParent.dir );

	fxParent.flags = FXP_DIR | FXP_ORIGIN;
	switch ( weapon ) {
	default:
	//Slightly fucked, netcode used an empty entity for lightning so use weapon 0
	case WP_LIGHTNING:
		fxHandle = cgs.fx.lightning.impact;
		break;
	case WP_MACHINEGUN:
		fxHandle = cgs.fx.machinegun.impact;
		break;
	case WP_SHOTGUN:
		fxHandle = cgs.fx.shotgun.impact;
		break;
	case WP_ROCKET_LAUNCHER:
		fxHandle = cgs.fx.rocket.impact;
		break;
	case WP_GRENADE_LAUNCHER:
		fxHandle = cgs.fx.grenade.impact;
		break;
	case WP_RAILGUN:
		fxHandle = cgs.fx.rail.impact;
		if ( clientNum >= 0 && clientNum < MAX_CLIENTS) {
			Byte4Copy( cgs.clientinfo[clientNum].c1RGBA, fxParent.color );
			Byte4Copy( cgs.clientinfo[clientNum].c2RGBA, fxParent.color2 );
		}
		break;
	case WP_BFG:
		fxHandle = cgs.fx.bfg.impact;
		break;
	case WP_PLASMAGUN:
		fxHandle = cgs.fx.plasma.impact;
		break;
	}
	trap_FX_Run( fxHandle, &fxParent, 0 );
}

